function parallelQuestion(): Question {
  const q = pick(PARALLEL_POOL);
  return {
    prompt: <>{q.what} — מותר או אסור?</>,
    options: ["מותר", "אסור"],
    answer: q.ok ? "מותר" : "אסור",
    explain: <>{q.why}</>,
  };
}

